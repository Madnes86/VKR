import type { OutputData } from '@editorjs/editorjs';

export interface DocArticle {
	id: number;
	slug: string;
	title: string;
	type: 'default' | 'optional';
	mass: number;
	content: OutputData;
}

export interface DocLink {
	id: number;
	name: string;
	type: 'default' | 'optional';
	is: number;
	to: number;
}

function p(text: string) {
	return { type: 'paragraph', data: { text } } as const;
}
function h(text: string, level: 2 | 3 = 2) {
	return { type: 'header', data: { text, level } } as const;
}
function ul(items: string[]) {
	return { type: 'list', data: { style: 'unordered', items } } as const;
}

